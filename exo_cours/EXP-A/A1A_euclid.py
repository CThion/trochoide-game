a, b = 343, 125
print(a, '=', b, '*', a//b, '+', a%b)          # solution A
print("%s   =   %s*%s+%s" % (a, b, a//b, a%b)) # solution B (much more flexible)
print(f"{a}    =     {b}*{a//b}+{a%b}")           # solution C (only Python >= 3.6)
